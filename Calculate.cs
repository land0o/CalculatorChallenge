using System;

namespace CalculatorChallenge
{
    public class Calculate
    {
        public int AddNumbers(int a, int b)
        {
            return a + b;
        }
        public int SubtractNumbers(int a, int b)
        {
            return a - b;
        }
        public int MultiplyNumbers(int a, int b)
        {
            return a * b;
        }
        public int DivideNumbers(int a, int b)
        {
            return a / b;
        }
    }
}